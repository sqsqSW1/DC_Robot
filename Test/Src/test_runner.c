/**
 ******************************************************************************
 * @file    test_runner.c
 * @brief   测试调度器 — 统一入口, 根据 test_config.h 开关调度各模块测试
 * @note
 *   ## 设计原则
 *   1. main.c 中只需一行 Test_Runner(), 不出现任何 #if TEST_XXX
 *   2. 每个模块的测试逻辑封装在各自的 _Test() 函数中
 *   3. 通过 test_config.h 的宏开关控制哪些测试参与编译
 *
 *   ## 新增测试模块的步骤
 *   1. test_config.h 中添加 #define TEST_NEWMODULE_ENABLE 1
 *   2. 对应模块的头文件中声明 void NewModule_Test(void);
 *   3. 对应模块的源文件中实现 NewModule_Test()
 *   4. 本文件中添加 #if TEST_NEWMODULE_ENABLE 块调用 NewModule_Test()
 *
 *   ## 注意事项
 *   - 测试函数应为阻塞式, 内部自带超时保护
 *   - 测试应在主循环启动前完成, 避免与正常业务逻辑冲突
 *   - 所有测试完成后, 各模块应恢复初始状态
 ******************************************************************************
 */

